    private void buttonAddActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_buttonAddActionPerformed
        // TODO add your handling code here:
        String name = textName.getText();
        String address = textAddress.getText();
        String email = textEmail.getText();
        String phone = textPhone.getText();
        if(name.isEmpty() || address.isEmpty() || email.isEmpty() || phone.isEmpty()){
            JOptionPane.showMessageDialog(rootPane, "Bạn chưa nhập đủ thông tin!");
        }
        else {

            Supplier s = new Supplier(name, email, phone, address);
            new SupplierDAO().addSupplier(s);
            JOptionPane.showMessageDialog(rootPane, "Bạn đã thêm thành công!");
            frm.updateTable();
            this.setVisible(false);
        }
    }//GEN-LAST:event_buttonAddActionPerformed

