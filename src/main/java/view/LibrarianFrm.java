    void showLibrarian(User u) {
        labelAddress.setText(u.getAddress());
        labelEmail.setText(u.getEmail());
        labelId.setText(u.getUserid());
        labelName.setText(u.getFullname());
        labelPhone.setText(u.getPhone());
        if(u.isStatus() == true){
            labelStatus.setText("Đang làm");
        }
        else {
            labelStatus.setText("Đã nghỉ");
        }
        labelUsername.setText(u.getUsername());
    }
