    private void buttonSearchActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_buttonSearchActionPerformed
        // TODO add your handling code here:
        String keyString = textSearchName.getText();
        if(keyString.isEmpty()){
            JOptionPane.showMessageDialog(rootPane, "Bạn chưa nhập tên thủ thư!");
        }
        else {
            try {
                listlibrarian = new UserDAO().getUserByName(keyString);
                showTable();
            } catch (SQLException ex) {
                Logger.getLogger(ManageLibrarianFrm.class.getName()).log(Level.SEVERE, null, ex);
            }
        }
    }//GEN-LAST:event_buttonSearchActionPerformed

