    private void showTable() {
        model.setRowCount(0);
        int index = 1;
        for(User u : listlibrarian){
            model.addRow(new Object[]{
                index++, u.getUserid(), u.getFullname(), u.getUsername(), u.isStatus()
            });
        }
    }

    void updateTable() {
        try {
            listlibrarian = new UserDAO().getUserByRoleid(2);
            showTable();
        } catch (SQLException ex) {
            Logger.getLogger(ManageLibrarianFrm.class.getName()).log(Level.SEVERE, null, ex);
        }
    }
