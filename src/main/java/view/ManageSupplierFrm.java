    private void buttonSearchActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_buttonSearchActionPerformed
        // TODO add your handling code here:
        String key = textSearch.getText();
        if(key.isEmpty()){
            JOptionPane.showMessageDialog(rootPane, "Bạn chưa nhập từ khóa!");
        }
        else {
            try {
                listsupplier = new SupplierDAO().getSupplierByKey(key);
                showTable();
            } catch (SQLException ex) {
                JOptionPane.showMessageDialog(rootPane, "Lỗi hiện nhà cung cấp!");
                Logger.getLogger(ManageSupplierFrm.class.getName()).log(Level.SEVERE, null, ex);
            }
        }
    }//GEN-LAST:event_buttonSearchActionPerformed

