    private void buttonAddActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_buttonAddActionPerformed
        // TODO add your handling code here:
        String nameString = textName.getText();
        String addressString = textAddress.getText();
        String emailString = textEmail.getText();
        String phoneString = textPhone.getText();
        Date dobDate = new Date();
        if(nameString.isEmpty() || addressString.isEmpty() || emailString.isEmpty() || phoneString.isEmpty()){
            JOptionPane.showMessageDialog(rootPane, "Bạn chưa nhập đủ thông tin!");
        }
        else {
            Reader r = new Reader(nameString, emailString, phoneString, addressString, new java.sql.Date(dobDate.getTime()));
            try {
                new ReaderDAO().addReader(r);
                JOptionPane.showMessageDialog(rootPane, "Đã thêm thành công!");
                frm.updateTable();
                this.setVisible(false);
            } catch (SQLException ex) {
                Logger.getLogger(AddReaderFrm.class.getName()).log(Level.SEVERE, null, ex);
            }
        }
    }//GEN-LAST:event_buttonAddActionPerformed

